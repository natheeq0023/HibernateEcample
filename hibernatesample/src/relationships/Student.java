package relationships;

public class Student {
	private int sid;
	private String sname;
	private int foriengid;
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getForiengid() {
		return foriengid;
	}
	public void setForiengid(int foriengid) {
		this.foriengid = foriengid;
	}

}
